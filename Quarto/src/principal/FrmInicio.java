    public class Imagen1 extends javax.swing.JPanel {

        public Imagen1() {
            this.setSize(200, 200);
        }

        public void paint(Graphics grafico) {
            Dimension ancho = getSize();

            ImageIcon Img = new ImageIcon(getClass().getResource("/Imagenes/FotoYordi.jpg"));
            grafico.drawImage(Img.getImage(), 0, 0, ancho.width, ancho.height, null);
            setOpaque(false);
            super.paintComponent(grafico);
        }

    }  
    
