    public void cargarFotos(){
        Imagen1 imagen = new Imagen1();
        jpYordi.add(imagen);
        jpYordi.repaint();
        Imagen2 imagen2 = new Imagen2();
        jpJulian.add(imagen2);
        jpJulian.repaint();
    }
    
    public void fechahora(){
        DateTimeFormatter fecha= DateTimeFormatter.ofPattern("dd-MM-yyyy");
        lblFecha.setText(fecha.format(LocalDateTime.now()));
        DateTimeFormatter hora = DateTimeFormatter.ofPattern("hh:mm a");
        lblHora.setText(hora.format(LocalDateTime.now()));
    }

