    private String ConvertirFechaString()
    {
        Date fecha = dtFechaNac.getDate();
        DateFormat formato = new SimpleDateFormat("dd/MM/yyyy");
        String fechaConvertida = formato.format(fecha);
        return fechaConvertida;
    }
    
    private String SeleccionRadioButton()
    {
        String res = "";
        if (rbMasculino.isSelected())
        {
            res = "Masculino";
        } else if (rbFemenino.isSelected())
        {
            res = "Femenino";
        }
        return res;
    }
    
    private void btnInsertarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInsertarActionPerformed
        String nombre = txtNombre.getText();
        String fechanac = ConvertirFechaString();
        String genero = SeleccionRadioButton();
        String datos = nombre + "," + fechanac + "," + genero;        
        usuarios.InsertarUsuarios(datos);
    }//GEN-LAST:event_btnInsertarActionPerformed

    private void btnMostrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnMostrarActionPerformed
        taMostrar.setText("");
        ArrayList<String> impresion = usuarios.LeerUsuarios();
        for (String datos : impresion)
        {
            taMostrar.append(String.valueOf(datos + "\r\n"));
        }
    }//GEN-LAST:event_btnMostrarActionPerformed

